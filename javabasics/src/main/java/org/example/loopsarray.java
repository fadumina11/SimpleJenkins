package org.example;

public class loopsarray {
    public class E5LoopArray {
        public static void main(String[] args) {
            double [] prices={10.5,20.5,25,30};

            for (int i=0;i<4;i++){
                System.out.println(prices[i]);
            }
        }

    }
    public static void main(String[] args) {
        String [] names={"Lana","Ana", "Maggy", "fab", "james"};
        int i=0;
        while (i<5) {
            System.out.println(names[i]);
            i++;
        }

    }
}
